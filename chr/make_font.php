// 0x0C: レンガ壁用タイル (palette 0 default colors を使用)
//   color 2 ($10 medium gray) = ブロック本体
//   color 3 ($00 dark gray)   = モルタル (継ぎ目)
//   2 段のレンガ (上段 aligned + 下段 offset) を 1 タイルに収め、繰返し配置で
//   水平/垂直に seamless にタイル化される。
$customTiles[0x0C] = [
    // bp0 (color 2 / 3 を区別する LSB)
    0xFF, 0x11, 0x11, 0x11, 0xFF, 0x44, 0x44, 0x44,
    // bp1 (color 2 / 3 とも MSB=1)
    0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF, 0xFF,
];

function build_bank(array $font5x7, array $customTiles): string
{
    $bank = str_repeat("\x00", 8192);

    foreach ($font5x7 as $i => $rows) {
        $ascii = 0x20 + $i;

        // --- table 0 (通常) ---
        $t0 = $ascii * 16;
        for ($y = 0; $y < 8; $y++) {
            $bank[$t0 + $y] = chr($rows[$y]);
        }

        // --- table 1 (インバース) ---
        // space (0x20) は空のままにして、nes_chr_bg(1) 時に未使用セルが埋まらないように
        if ($ascii === 0x20) {
            continue;
        }
        $t1 = 0x1000 + $ascii * 16;
        for ($y = 0; $y < 8; $y++) {
            // 7 列幅 ($FE) でインバース (グリフ部分が 0、背景が 1)
            // 大文字/数字が 7 列幅になったのに合わせて拡大 (5x7 glyph も同じ幅で punch)
            $bank[$t1 + $y] = chr($rows[$y] ^ 0xFE);
        }
    }

    // カスタムタイルを table 0 に配置 (0x00-0x1F は ASCII フォント未使用域)
    foreach ($customTiles as $tileIndex => $data) {
        $offset = $tileIndex * 16;
        for ($b = 0; $b < 16; $b++) {
            $bank[$offset + $b] = chr($data[$b]);
        }
    }

    return $bank;
}

// CNROM (マッパー 3) 前提で 4 × 8KB = 32KB CHR-ROM
//   Bank 0: 標準フォント + インバース (上記 build_bank)
//   Bank 1-3: bank 0 のコピー (プレゼン用に自分で差し替える)
// bank 1-3 に独自タイルを詰めたい場合は $banks を書き換える
$bank0 = build_bank($font5x7, $customTiles);
$banks = [
    0 => $bank0,
    1 => $bank0,
    2 => $bank0,
    3 => $bank0,
];

$chr = '';
for ($i = 0; $i < 4; $i++) {
    $chr .= $banks[$i];
}
assert(strlen($chr) === 32768);

$outPath = __DIR__ . '/font.chr';
file_put_contents($outPath, $chr);
fprintf(STDERR, "[make_font] wrote %s (%d bytes, 4 × 8KB banks)\n", $outPath, strlen($chr));
