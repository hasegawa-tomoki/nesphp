        for ($x = 0; $x < 8; $x++) {
            $c = $cloudColor[$cloud[$r0 + $y][$c0 + $x]];
            $b0 = ($b0 << 1) | ($c & 1);
            $b1 = ($b1 << 1) | (($c >> 1) & 1);
        }
        $bp0[] = $b0;
        $bp1[] = $b1;
    }
    $customTiles[$tile_idx] = array_merge($bp0, $bp1);
}

