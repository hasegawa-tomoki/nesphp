<?php
// 配列書換 / append / ネストの動作確認
$xs = [10, 20, 30];
$xs[1] = 99;          // index write → [10, 99, 30]
$xs[] = 77;           // append → [10, 99, 30, 77]
$n = count($xs);
for ($i = 0; $i < $n; $i++) {
    echo $xs[$i];
    echo " ";
}
// ここで出力: "10 99 30 77 "

// ネスト: 行列 2x3
$m = [[1, 2, 3], [4, 5, 6]];
echo $m[0][0];        // 1
echo $m[0][2];        // 3
echo $m[1][1];        // 5
// 出力: "135"
